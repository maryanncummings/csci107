import turtle  # importing turtle module/library
gs = turtle.Screen() # creates a graphics window
gs.bgcolor("light blue")
gs.title("My first turtle")
alex = turtle.Turtle()  # starts at 0,0; is black; is 2 pixels wide
alex.forward(150)
alex.left(90)
alex.forward(75)
alex.left(90)
alex.forward(150)
alex.left(90)
alex.forward(75)

